using System;
using UIKit;

namespace DynamicTextInputCellExample
{
	///<summary>
	///
	///</summary>
	class DynamicTextViewCell : UITableViewCell
	{
		public event EventHandler didChange;
		public event EventHandler didBeginEditing;
		//========================================================================================================================================
		//  PRIVATE CLASS PROPERTIES
		//========================================================================================================================================
		private UITextView _textView;
		//========================================================================================================================================
		//  PUBLIC CLASS PROPERTIES
		//========================================================================================================================================
		public UITextView TextView {
			get
			{ return this._textView; 
			}
			set
			{
				return; 
			}
		}
		//========================================================================================================================================
		//  Constructor
		//========================================================================================================================================
		/// <summary>
		/// Initializes a new instance of the <see cref="SaltAndPepper.ios.DynamicTextViewCell"/> class.
		/// </summary>
		public DynamicTextViewCell () : base (UITableViewCellStyle.Default, "textMessage")
		{
			_textView = new UITextView ();

		}
		//========================================================================================================================================
		//  PUBLIC OVERRIDES
		//========================================================================================================================================
		//========================================================================================================================================
		//  PUBLIC METHODS
		//========================================================================================================================================
		//========================================================================================================================================
		//  PRIVATE METHODS
		//========================================================================================================================================
	}
}